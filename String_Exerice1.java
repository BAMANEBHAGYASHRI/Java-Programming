import java.util.*;
public class String_Exerice1 {
   public static void main(String[] args) {
    Scanner sc=new Scanner(System.in);
    String str=sc.next();
    String result=sc.next();

    for(int i=0;i<str.length();i++){
        if(str.charAt(i)=='e'){
            result +='i';
        }
        else{
            result +='i';
        }
    }
    System.out.println(result);
   } 
    
}
